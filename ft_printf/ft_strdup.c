#include "libftprintf.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	char	*dup;

	len = ft_strlen(s1);
	dup = malloc(len + 1);
	if (dup)
		ft_memcpy(dup, s1, len);
	else
		return (NULL);
	*(dup + len) = '\0';
	return (dup);
}
